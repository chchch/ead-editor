			<p id="shortdesc">The <emph render="italic">Attuvaitāṉupavam</emph>, “Experience of Non-Duality” (Sanskrit <emph render="italic">advaita</emph> + <emph render="italic">anubhava</emph>), is a philosophical text, of which there seems to exist no printed edition yet. According to the preface (f76) and the final colophon (f95), the author is Rāmakavi, disciple of Civarāmakavi (Sanskrit Śivarāmakavi), himself disciple of Navamakāleṭcumi Nāraciṅkatēcikar (Sanskrit Navamahālakṣmī Nārasiṃhadeśika). In the present manuscript the work consists of 128 stanzas.</p>
            
